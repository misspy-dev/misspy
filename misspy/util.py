def nonecheck(arg):
    """A function that simply returns False if the value of the variable passed as an argument is None, and True if it is not None

    Args:
        arg: Variable (value) that determines whether it is None

    Returns:
        bool: True or False
    """

    if arg is None:
        return False
    else:
        return True

